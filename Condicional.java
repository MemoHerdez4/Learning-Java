public class Condicional{
    public static void main(String[] args) {
        int number1 = 5;
        int number2 = 10;
        int number3 = 15;

        if (number1 < number2 && number2 < number3){
            System.out.println("Verdadero");
        } else {  
            System.out.println("Falso");
        }
    }
    
}